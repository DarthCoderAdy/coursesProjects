class Calculator {



    int secti(int a, int b) {
        a + b
    }

    int odecti(int a, int b) {
        a - b
    }

    int nasob (int a, int b) {
        a * b
    }

    double vydel(double  a, double b) {
        if (b !== 0) {
            a / b
        } else {
            throw new RuntimeException("cannot divide by zero")
        }
    }


}
